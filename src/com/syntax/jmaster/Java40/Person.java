package com.syntax.jmaster.Java40;

public class Person {
    private String ten;
    String diachi;

    public String getTen() {
        return ten;
    }

    public Person(String ten, String diachi) {
        this.ten = ten;
        this.diachi = diachi;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getDiachi() {
        return diachi;
    }

    public void setDiachi(String diachi) {
        this.diachi = diachi;
    }
}
