/**
 * The type Simple calculator.
 */
public class SimpleCalculator {
    /**
     * Add int.
     *
     * @param numberA the number a
     * @param numberB the number b
     * @return the int
     */
    public int add(int numberA, int numberB)
    {
        return numberA + numberB;
    }
}
